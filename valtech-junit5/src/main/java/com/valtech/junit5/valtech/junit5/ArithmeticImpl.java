package com.valtech.junit5.valtech.junit5;

public class ArithmeticImpl implements Arithmetic{

	public int add(int a, int b) {
		return a+b;
	}

	public int sub(int a, int b) {
		return a-b;

	}

	public int mult(int a, int b) {
		return a*b;
	}

	public int divd(int a, int b) {
		return a/b;
	}

}
